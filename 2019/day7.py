class IntcodeComputer():
    def __init__(self, input, prog, child=None, name='A'):
        self.input = input
        self.ip = 0
        self.input_pointer   = 0
        self.diagnostic_code = 0
        self.name = name
        self.prog = prog
        self.run = True

        self.child = child
        self.opcodes = {
         '01' : self.add,
         '02' : self.mul,
         '03' : self.save,
         '04' : self.out,
         '05' : self.jmpt,
         '06' : self.jmpf,
         '07' : self.lt,
         '08' : self.eq,
        }

    def add(self, modes=[]):
        result_loc         = self.prog[self.ip+3]
        opr0, opr1 = self.prog[self.ip+1], self.prog[self.ip+2]
        self.prog[result_loc] = (self.prog[opr0] if modes[-1]=='0' else opr0) + (self.prog[opr1] if modes[-2]=='0' else opr1)
        self.ip += 4

    def mul(self, modes=[]):
        result_loc         = self.prog[self.ip+3]
        opr0, opr1 = self.prog[self.ip+1], self.prog[self.ip+2]
        self.prog[result_loc] = (self.prog[opr0] if modes[-1]=='0' else opr0) * (self.prog[opr1] if modes[-2]=='0' else opr1)
        self.ip += 4

    def save(self, modes=[]):

        if modes[-1] == '0':
            self.prog[self.prog[self.ip+1]] = self.input[self.input_pointer]
        else:
            self.prog[self.ip+1] = self.input[self.input_pointer]

        self.input_pointer += 1
        self.ip += 2

    def out(self, modes=[]):
        self.diagnostic_code = self.prog[self.prog[self.ip+1]] if modes[-1] == '0' else self.prog[self.ip+1]

        self.run = False
        self.ip += 2
        self.child.input.append(self.diagnostic_code)
        self.child.execute()
        self.run = True

    def jmpt(self,  modes=[]):
        opr0 = self.prog[self.prog[self.ip+1]] if modes[-1] == '0' else self.prog[self.ip+1]
        opr1 = self.prog[self.prog[self.ip+2]] if modes[-2] == '0' else self.prog[self.ip+2]
        if opr0 != 0:
            self.ip = opr1
        else:
            self.ip += 3

    def jmpf(self, modes=[]):
        opr0 = self.prog[self.prog[self.ip+1]] if modes[-1] == '0' else self.prog[self.ip+1]
        opr1 = self.prog[self.prog[self.ip+2]] if modes[-2] == '0' else self.prog[self.ip+2]
        if opr0 == 0:
            self.ip = opr1
        else:
            self.ip += 3

    def lt(self, modes=[]):
        opr0 = self.prog[self.prog[self.ip+1]] if modes[-1] == '0' else self.prog[self.ip+1]
        opr1 = self.prog[self.prog[self.ip+2]] if modes[-2] == '0' else self.prog[self.ip+2]

        self.prog[self.prog[self.ip+3]] = 1 if opr0 < opr1 else 0
        self.ip += 4

    def eq(self, modes=[]):
        opr0 = self.prog[self.prog[self.ip+1]] if modes[-1] == '0' else self.prog[self.ip+1]
        opr1 = self.prog[self.prog[self.ip+2]] if modes[-2] == '0' else self.prog[self.ip+2]

        self.prog[self.prog[self.ip+3]] = 1 if opr0 == opr1 else 0
        self.ip += 4

    def execute(self):
        self.run = True
        while (self.run):
            opcode = str(self.prog[self.ip])[-2:]
            if (opcode == '99'):
                self.run = False
            else:
                opcode = '0'+opcode if (len(opcode) == 1) else opcode
                modes = (['0','0','0'] + [i for i in str(self.prog[self.ip])[:-2]])[-3:]

                self.opcodes[opcode](modes)


def generate_phases(lower, upper):
    phases = []
    for p1 in range(lower, upper):
        for p2 in [i for i in range(lower, upper) if i != p1]:
            for p3 in [i for i in range(lower, upper) if i not in [p1, p2]]:
                for p4 in [i for i in range(lower, upper) if i not in [p1, p2,p3]]:
                    for p5 in [i for i in range(lower, upper) if i not in [p1, p2,p3,p4]]:
                        phases.append([p1,p2,p3,p4,p5])
    return phases

